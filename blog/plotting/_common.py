# ── Helpers ──────────────────────────────────────────────────────────────────

def apu_totals(data_dict, apu_map):
    out = {}
    for a in range(4):
        s = np.zeros(5)
        for d in apu_map[a]:
            s += np.array(data_dict[d])
        out[a] = s
    return out


def node_total(data_dict):
    s = np.zeros(5)
    for d in data_dict:
        s += np.array(data_dict[d])
    return s


def load_sweep(mode):
    """Load sweep CSV for a given mode (cpx/tpx/spx)."""
    path = os.path.join(LOG_DIR, f'010_{mode}', 'summary.csv')
    return pd.read_csv(path)


def savefig(fig, name):
    path = os.path.join(FIG_DIR, name)
    fig.savefig(path, dpi=180, bbox_inches='tight', facecolor='white')
    plt.close(fig)
    print(f'  saved → {path}')


def style_ax(ax, ylabel=None, xlabel=None, title=None, grid_axis='y'):
    if ylabel: ax.set_ylabel(ylabel, fontsize=11)
    if xlabel: ax.set_xlabel(xlabel, fontsize=11)
    if title:  ax.set_title(title, fontsize=13, fontweight='bold')
    ax.grid(axis=grid_axis, alpha=0.2)
    ax.set_axisbelow(True)
